package danieljpa.entidades;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToOne;


/**
 *
 * @author Usuario
 */
@Entity
public class Libro implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    private String titulo;
    private Integer anio;
    private Integer ejemplares;
    private Integer Prestados;
    private Integer Restantes;
    private Boolean alta=true;
    @OneToOne
    private Autor autor;
    @OneToOne
    private Editorial editorial;

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public Integer getAnio() {
        return anio;
    }

    public void setAnio(Integer anio) {
        this.anio = anio;
    }

    public Integer getEjemplares() {
        return ejemplares;
    }

    public void setEjemplares(Integer ejemplares) {
        this.ejemplares = ejemplares;
    }

    public Integer getPrestados() {
        return Prestados;
    }

    public void setPrestados(Integer Prestados) {
        this.Prestados = Prestados;
    }

    public Integer getRestantes() {
        return Restantes;
    }

    public void setRestantes(Integer restantes) {
        this.Restantes = restantes;
    }

    public Boolean getAlta() {
        return alta;
    }

    public void setAlta(Boolean alta) {
        this.alta = alta;
    }

    public Autor getAutor() {
        return autor;
    }

    public void setAutor(Autor autor) {
        this.autor = autor;
    }

    public Editorial getEditorial() {
        return editorial;
    }

    public void setEditorial(Editorial editorial) {
        this.editorial = editorial;
    }

    
    
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Libro)) {
            return false;
        }
        Libro other = (Libro) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Libro->" + "id=" + id + "\n"
                + "     titulo=" + titulo + "\n"
                + "     anio=" + anio + "\n"
                + "     ejemplares=" + ejemplares + "\n"
                + "     Prestados=" + Prestados + "\n"
                + "     Restantes=" + Restantes + "\n"
                + "     alta=" + alta + "\n"
                + "     autor=" + autor + "\n"
                + "     editorial=" + editorial + "<-";
    }

    

   
}
